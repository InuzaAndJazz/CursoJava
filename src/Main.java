
public class Main {

	public static void main(String[] args) {
		String palabra = "Hola mundo";
		int entero = -100;
		double decimal = -1.6;
		boolean binario = true; // false

		// *Tipo de variable* *nombre de la variable* = *valor*;

		System.out.println("La variable [palabra] es igual a: [" + palabra + "]");
		System.out.println("La variable [entero] es igual a: [" + entero + "]");
		System.out.println("La variable [decimal] es igual a: [" + decimal + "]");
		System.out.println("La variable [binario] es igual a: [" + binario + "]");
	}

}
